/** 国际化状态接口 */
interface I18nState {
  lang: Lang;                    // 当前语言（zh 或 en）
  setLang: (lang: Lang) => void; // 切换语言
  t: (key: string) => string;    // 翻译函数 — 根据当前语言返回对应文本，未找到时返回 key 本身
}

export const useI18n = create<I18nState>((set, get) => ({
  lang: "zh",
  setLang: (lang) => set({ lang }),
  // 翻译函数：从对应语言的字典中查找 key，找不到则原样返回
  t: (key) => {
    const dict = get().lang === "zh" ? zh : en;
    return dict[key] || key;
  },
}));
